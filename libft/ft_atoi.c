#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i[3];

	i[0] = 0;
	i[1] = 0;
	i[2] = -1;
	while (str[i[1]])
	{
		if (((str[i[1]] < 8 || str[i[1]] > 13) && str[i[1]] != ' ') || i[0])
		{
			if ((str[i[1]] == '-' || str[i[1]] == '+') && !i[0] &&
				(str[i[1] + 1] > 47 && str[i[1] + 1] < 58))
				i[2] = ((i[2]) - (44 - str[i[1]])) / 2;
			else if ((str[i[1]] == '0' && ((str[i[1] + 1] - '0') < 0) &&
				!(i[0])) || (str[i[1]] > 57 || str[i[1]] < 48) || (str[i[1]]
				== '-' && !(str[i[1] + 1] > 48 && str[i[1] + 1] < 57)) || (str
				[i[1]] == '+' && !(str[i[1] + 1] > 48 && str[i[1] + 1] < 57)))
				break ;
			else if (i[0] >= 1000000000)
				return (i[2]);
			else
				i[0] = i[0] * 10 + (str[i[1]] - '0');
		}
		i[1]++;
	}
	return (i[0] * ((i[2] * -2) - 1));
}
